    private void btnSökLKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSökLKActionPerformed
        DefaultListModel dmKurser = new DefaultListModel();

        //sätter vilket format datumet har
        kursStartDatum.setDateFormatString("yyyy-MM-dd");
        DateFormat startDatum = new SimpleDateFormat("yyyy-MM-dd");

        kursSlutDatum.setDateFormatString("yyyy-MM-dd");
        DateFormat slutDatum = new SimpleDateFormat("yyyy-MM-dd");

        String lärarensFörnamn = fldFörnamnLärare.getText();
        String lärarensEfternamn = fldEfternamnLärare.getText();

        try {
            try {
                //Kod för att ta fram kurser mellan två datum som en specifik lärare ansvarar för
                String lärarensID = databas.fetchSingle("select larar_id from larare where fornamn='" + lärarensFörnamn + "' and efternamn='" + lärarensEfternamn + "';");
                ArrayList<String> kurser = new ArrayList<>();
                kurser = databas.fetchColumn("select kursnamn from kurs join larare on kurslarare=larar_id where larar_id=" + lärarensID + " and kursstart >='" + startDatum.format(kursStartDatum.getDate()) + "' and kursslut<='" + slutDatum.format(kursSlutDatum.getDate()) + "';");

                //Om inga kurser finns mellan dessa datum kommer detta medelande
                if (kurser.isEmpty()) {
                    dmKurser.addElement("Finns inga kurser mellan dessa datum");
                } else {
                    for (String kurs : kurser) {
                        dmKurser.addElement(kurs);
                    }
                }

                jListKurserLärare.setModel(dmKurser);

            } catch (InfException ex) {
                Logger.getLogger(KurserWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Ange Lärare och datum");
        }

    }//GEN-LAST:event_btnSökLKActionPerformed

