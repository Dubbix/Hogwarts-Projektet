    private void btnSökBetygActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSökBetygActionPerformed
        String fornamn = (String) fldElevnamn.getText();
        String efternamn = (String) fldEfternamn.getText();
        DefaultListModel listModel = new DefaultListModel();

        Validering val = new Validering();

        //Tittar om någon av fälten är tomma och visar då ett medelande med hjälp av valideringsklassen
        if (!val.textNotEmpty(fldElevnamn, "Ange förnamn")) {
            return;
        }
        if (!val.textNotEmpty(fldEfternamn, " Ange efternamn")) {

            return;
        }

        try {
            try {
                //Fyller en arraylist med raderna från specifika columner i databasen
                ArrayList<HashMap<String, String>> kurser = databas.fetchRows("select KURSNAMN,KURSBETYG from kurs join HAR_BETYG_I on kurs.kurs_id=HAR_BETYG_I.kurs_id join elev on har_betyg_i.ELEV_ID=elev.ELEV_ID where fornamn='" + fornamn + "' and efternamn='" + efternamn + "';");

                for (int i = 0; i < kurser.size(); i++) {
                    String kursNamn = kurser.get(i).get("KURSNAMN");
                    String kursBetyg = kurser.get(i).get("KURSBETYG");

                    listModel.addElement(kursNamn + " " + kursBetyg);
                }

                jListKurser.setModel(listModel);
            } catch (InfException ex) {
                JOptionPane.showMessageDialog(null, "Angivet elev namn finns ej");
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Angiven elev finns ej");
        }
    }//GEN-LAST:event_btnSökBetygActionPerformed

