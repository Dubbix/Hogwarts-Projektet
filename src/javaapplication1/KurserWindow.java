    private void btnSökKursBetygActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSökKursBetygActionPerformed
        String fornamn = (String) textFieldFörnamnEK.getText();
        String efternamn = (String) txtFieldEfternamnEK.getText();

        //Skapar "modeller" som håller 
        DefaultListModel listModelStartdatum = new DefaultListModel();
        DefaultListModel listModelSlutdatum = new DefaultListModel();
        DefaultListModel listModelKursNamn = new DefaultListModel();
        ArrayList<String> kursNamn = new ArrayList<>();
        Validering val = new Validering();

        //Tittar om någon av fälten är tomma och visar då ett medelande med hjälp av valideringsklassen
        if (!val.textNotEmpty(textFieldFörnamnEK, "Ange förnamn")) {
            return;
        }
        if (!val.textNotEmpty(txtFieldEfternamnEK, " Ange efternamn")) {

            return;
        }

        try {
            try {
                //Skapar arrylist med startdatum och slutdatum raderna från databasen
                ArrayList<HashMap<String, String>> startdatumSlutdatum = databas.fetchRows("select kursstart,kursslut from kurs join har_betyg_i on kurs.kurs_id=har_betyg_i.KURS_ID join elev on har_betyg_i.elev_id=elev.ELEV_ID where fornamn='" + fornamn + "' and efternamn='" + efternamn + "';");

                for (int i = 0; i < startdatumSlutdatum.size(); i++) {
                    String startdatum = startdatumSlutdatum.get(i).get("KURSSTART");
                    String slutdatum = startdatumSlutdatum.get(i).get("KURSSLUT");

                    listModelStartdatum.addElement(startdatum);
                    listModelSlutdatum.addElement(slutdatum);
                }

                //Gör så att jlisterna använder sig av modellerna som skapades tidigare
                jListStartdatum.setModel(listModelStartdatum);
                jListSlutDatum.setModel(listModelSlutdatum);

                //raddar upp kurser efter lärarens förnamn och efternamn
                kursNamn = databas.fetchColumn("select kursnamn from kurs join har_betyg_i on kurs.kurs_id=har_betyg_i.KURS_ID join elev on har_betyg_i.elev_id=elev.ELEV_ID where fornamn='" + fornamn + "' and efternamn='" + efternamn + "';");

                for (int i = 0; i < kursNamn.size(); i++) {
                    listModelKursNamn.addElement(kursNamn.get(i));
                }

                jListKursnamn.setModel(listModelKursNamn);

            } catch (InfException exl) {
                JOptionPane.showMessageDialog(null, "Angivet elev namn finns ej");
            }

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Angiven elev finns ej");
        }
    }//GEN-LAST:event_btnSökKursBetygActionPerformed

